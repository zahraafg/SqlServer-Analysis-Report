	COUNT(INV_FICHENO) AS CEK_SAYI
FROM OPR_INVOICE
LEFT JOIN SYS_USERS_
ON USR_CSCID = INV_CSC_ID
WHERE INV_TRCODE = 1
AND INV_STATUS = 1
GROUP BY
	CAST(INV_DATETIME AS DATE),
	USR_NAME
),
CTE2 AS(
SELECT *,
ROW_NUMBER() OVER(
PARTITION BY SALES_DATE
ORDER BY TOTAL_SALES DESC
) AS RN
FROM CTE
)
SELECT *
FROM CTE2
WHERE RN = 1;

