USE POSFAPCENTER;

-- Her invoice da nece ferqli mehsul var.
SELECT TOP 100
	INV_FICHENO,
	INV_DATETIME,
	INV_LINECOUNT,
	INV_TERMINAL,
	COUNT(DISTINCT STL_ITMCODE) STL_ITMCODE
	--SUM(STL_AMOUNT) AS STL_AMOUNT,
	--SUM(STL_AMOUNT * STL_PRICE) AS TOTAL_SALES
FROM OPR_INVOICE
LEFT JOIN OPR_STLINE
ON STL_FICHENO = INV_FICHENO
WHERE INV_TRCODE = 1
AND INV_STATUS = 1
GROUP BY
	INV_FICHENO,
	INV_DATETIME,
	INV_LINECOUNT,
	INV_TERMINAL
ORDER BY STL_ITMCODE DESC;


