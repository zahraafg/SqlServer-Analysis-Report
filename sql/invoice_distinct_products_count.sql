SELECT 
	STL_FICHENO,
	STL_AMOUNT,
	STL_RTNFICHENO,
	STL_RTNAMOUNT,
	ITM_CODE,
	ITM_NAME
FROM OPR_STLINE
LEFT JOIN CRD_ITEMS
ON ITM_CODE = STL_ITMCODE
LEFT JOIN OPR_INVOICE
ON INV_FICHENO = STL_FICHENO
WHERE 
( 
	STL_FICHENO = '001002250420144515'
	OR STL_RTNFICHENO = '001002250420144515'
)
AND STL_TRCODE = 1
AND STL_STATUS = 1;
