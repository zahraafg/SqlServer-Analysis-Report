WITH SALES AS (
    SELECT 
        I.ITM_CODE,
        I.ITM_NAME,
        ISLS.INV_FICHENO,
        ISLS.INV_CLCARDCODE,
        USR_NAME,
        STG_ERPCODE,
        STG_NAME,

        SUM(SSLS.STL_AMOUNT) AS SATIS_MIQDAR,
        SUM(SSLS.STL_AMOUNT * SSLS.STL_PRICE) AS SATIS_MEBLEG

    FROM CRD_ITEMS I
    LEFT JOIN OPR_STLINE SSLS 
        ON SSLS.STL_ITMCODE = I.ITM_CODE
    LEFT JOIN OPR_INVOICE ISLS 
        ON ISLS.INV_FICHENO = SSLS.STL_FICHENO
    LEFT JOIN CRD_STORAGES 
        ON STG_ID = ISLS.INV_OBJ_ID
    LEFT JOIN SYS_USERS_ 
        ON USR_ID = ISLS.INV_USR_ID

    WHERE I.ITM_CODE = '128787'
      AND ISLS.INV_TRCODE = 1
      AND ISLS.INV_STATUS = 1
      AND MONTH(ISLS.INV_DATETIME) = 4

    GROUP BY 
        I.ITM_CODE,
        I.ITM_NAME,
        ISLS.INV_FICHENO,
        ISLS.INV_CLCARDCODE,
        USR_NAME,
        STG_ERPCODE,
        STG_NAME
),

RETURNS AS (
    SELECT 
        I.ITM_CODE,
        ISLS.INV_RTNFICHENO,

        SUM(SRTN.STL_RTNAMOUNT) AS QAYTARMA_MIQDAR,
        SUM(SRTN.STL_RTNAMOUNT * SRTN.STL_PRICE) AS QAYTARMA_MEBLEG

    FROM CRD_ITEMS I
    LEFT JOIN OPR_STLINE SRTN 
        ON SRTN.STL_ITMCODE = I.ITM_CODE
    LEFT JOIN OPR_INVOICE ISLS 
        ON ISLS.INV_FICHENO = SRTN.STL_FICHENO

    WHERE I.ITM_CODE = '128787'
      AND ISLS.INV_TRCODE = 2
      AND ISLS.INV_STATUS = 1
      AND MONTH(ISLS.INV_DATETIME) = 4

    GROUP BY 
        I.ITM_CODE,
        ISLS.INV_RTNFICHENO
)

SELECT 
    S.STG_ERPCODE,
    S.STG_NAME,
    S.ITM_CODE,
    S.ITM_NAME,
    S.USR_NAME,
    S.INV_FICHENO,
    S.INV_CLCARDCODE,

    COUNT(S.INV_FICHENO) AS CEK_SAYI,
    SUM(S.SATIS_MIQDAR) AS SATIS_MIQDAR,
    SUM(S.SATIS_MEBLEG) AS SATIS_MEBLEG,

    COUNT(R.INV_RTNFICHENO) AS RTN_CEK,
    SUM(R.QAYTARMA_MIQDAR) AS QAYTARMA_MIQDAR,
    SUM(R.QAYTARMA_MEBLEG) AS QAYTARMA_MEBLEG

FROM SALES S
LEFT JOIN RETURNS R
    ON R.ITM_CODE = S.ITM_CODE
    AND R.INV_RTNFICHENO = S.INV_FICHENO

GROUP BY 
    S.STG_ERPCODE,
    S.STG_NAME,
    S.ITM_CODE,
    S.ITM_NAME,
    S.USR_NAME,
    S.INV_FICHENO,
    S.INV_CLCARDCODE;
