WITH SATIS AS(
SELECT 
	STL_ITMCODE,
	COUNT(DISTINCT CASE WHEN INV_TRCODE = 1 THEN INV_FICHENO END) AS CEK,
	SUM(STL_AMOUNT) AS SATIS_MIQDAR,
	SUM(STL_AMOUNT * STL_PRICE) AS SATIS_MEBLEG
FROM OPR_STLINE
LEFT JOIN OPR_INVOICE
ON INV_FICHENO = STL_FICHENO
WHERE INV_STATUS = 1
AND MONTH(INV_DATETIME) = 4
GROUP BY STL_ITMCODE
),
QAYTARMA AS(
SELECT 
	STL_ITMCODE,
	COUNT(DISTINCT CASE WHEN INV_TRCODE = 2 THEN INV_RTNFICHENO END) AS RTN_CEK,
	SUM(STL_RTNAMOUNT) AS QAYTARMA_MIQDAR,
	SUM(STL_RTNAMOUNT * STL_PRICE) AS QAYTARMA_MEBLEG
FROM OPR_STLINE
LEFT JOIN OPR_INVOICE
ON INV_FICHENO = STL_FICHENO
WHERE INV_STATUS = 1
AND MONTH(INV_DATETIME) = 4
GROUP BY STL_ITMCODE
)
SELECT 

	'128787' AS ITM_CODE,

	ISNULL(S.CEK, 0) AS CEK,
	ISNULL(S.SATIS_MIQDAR, 0) AS SATIS_MIQDAR,
	ISNULL(S.SATIS_MEBLEG, 0) AS SATIS_MEBLEG,

	ISNULL(Q.RTN_CEK, 0) AS RTN_CEK,
	ISNULL(Q.QAYTARMA_MIQDAR, 0) AS QAYTARMA_MIQDAR,
	ISNULL(Q.QAYTARMA_MEBLEG, 0) AS QAYTARMA_MEBLEG

FROM SATIS S
LEFT JOIN QAYTARMA Q
ON Q.STL_ITMCODE = S.STL_ITMCODE
WHERE S.STL_ITMCODE = '128787'
OR Q.STL_ITMCODE = '128787';

