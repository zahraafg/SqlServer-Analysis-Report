USE POSFAPCENTERPLUS;

-- Bir çek üzrə hansı məhsullar satılıb və həmin məhsullar üzrə hansı qaytarmalar olub?
SELECT DISTINCT
	STL_FICHENO,
	STL_ITMCODE,
	ITM_NAME,
	STL_AMOUNT,
	STL_RTNFICHENO,
	STL_RTNAMOUNT
FROM OPR_STLINE
LEFT JOIN CRD_ITEMS
ON ITM_CODE = STL_ITMCODE
WHERE STL_FICHENO = '101008260301194921'
or STL_RTNFICHENO = '101008260301194921'; 
